    private void OpenImageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenImageButtonActionPerformed
        JFileChooser chooser = new JFileChooser(); //Create object for JFileChooser
        int response = chooser.showOpenDialog(null); //Opening up the dialog to choose a file (image)
        if (response == JFileChooser.APPROVE_OPTION)
        {
            File f = chooser.getSelectedFile(); //Chooses the file (image) in the dialog to be added
            String filename = f.getAbsolutePath();
            BrowseInputTextField.setText(filename); //Obtains the path of the file in the text field through AbsolutePath
        }
        
        else {
        JOptionPane.showMessageDialog(null, "The image was not obtained, cancelled.");
    }

    }//GEN-LAST:event_OpenImageButtonActionPerformed

